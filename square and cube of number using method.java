// square of number using method 

class sqrt {
    public static void main(String[] args) {
        square(5);
        cube(5);
    }
        static void square(int n1){
            System.out.println(n1*n1);
        }
        static void cube(int n2){
            System.out.println(n2*n2*n2);
        }
    
}